#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	ssize_t	i;
	char	*result;

	if (!s || !f)
		return (NULL);
	result = ft_calloc(ft_strlen(s) + 1, 1);
	if (!result)
		return (NULL);
	i = 0;
	while (s[i])
	{
		result[i] = f(i, s[i]);
		i++;
	}
	return (result);
}
